// Напишете програма, която прочита цяло число N от конзолата и извежда N/2, ако се дели на 2, и 3*N, ако не се дели на 2.
// Вход: 12,  Изход: 6
// Вход: 5,  Изход: 15

#include <iostream>

using namespace std;

int main() {
    int n;
    cin >> n;

    cout << (n % 2 == 0) * (n / 2) + (n % 2 != 0) * (3 * n) << endl;

    return 0;
}