// Да се напише програма, която приема поток от числа. Числата се събират, докато не срещне числото 0.
// Тогава трябва да се отпечата акумулираният резултат и програмата да приключи.

// Вход: 1 2 3 4 5 6 0,  Изход: 21
// Вход: 9 9 1 0,  Изход: 19
// Вход: 5 4 3 0,  Изход: 12

#include <iostream>

using namespace std;

int main() {
    int sum = 0;
    int number;

    do {
        cin >> number;
        sum += number;
    } while (number != 0);

    cout << sum << endl;

    return 0;
}